## ----------------------------------------------------------------------------
## Dict 자료형 살펴보기
## - 컨테이너 타입 중 맵핑 타입 => Key:Value
## - 형식 : {Key1:Value1, Key2:Value2, ...}
## - 특징 : Key로 Value를 찾음! 인덱스 필요 없음!!!
## ----------------------------------------------------------------------------
## dict 자료형의 원소/요소 읽기/변경/추가하기
## ----------------------------------------------------------------------------
## => 동일한 Key 존재하면 Value가 갱신/업데이트 됨!
d1 = {'name':'홍길동', 'age':10, 'jumsu':[90, 99, 100], 'rank':10, 'age':759}

print(f"d1 => {type(d1)}, {len(d1)}개, {d1}")

## ----------------------------------------------------------------------------
## dict 원소/요소 읽기 => 객체변수명[ Key ]
## ----------------------------------------------------------------------------
print(f"name  => {d1['name']}")
print(f"jumsu => {d1['jumsu']}")
print(f"마지막 과목 jumsu => {d1['jumsu'][-1]}")

## ----------------------------------------------------------------------------
## dict 원소/요소 변경 => 객체변수명[ Key ] = 새로운 값
## ----------------------------------------------------------------------------
## 개명 '홍길동' ===> '홍이장군'
d1['name'] = '홍이장군'
print(d1)

## 점수 [90, 99, 100] ==> 98
d1['jumsu'] = 98
print(d1)

## ----------------------------------------------------------------------------
## dict 원소/요소 추가 => 객체변수명[ 새로운 Key ] = 값
## ----------------------------------------------------------------------------
## 성별 추가
d1['gender'] = '여'

## 아이디 추가
d1['id'] = 'Hong2020'
print(d1)

## ----------------------------------------------------------------------------
## dict 원소/요소 삭제 => del 객체변수명[Key]
##                    => del(객체변수명[Key])
## ----------------------------------------------------------------------------
## rank 키 삭제
del d1['rank']
print(d1)

## name 키 삭제
del d1['name']
print(d1)