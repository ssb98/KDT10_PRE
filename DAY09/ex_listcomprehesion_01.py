## ------------------------------------------------------------------------------
## List Comperhension : [] + for~in + if~else
## ------------------------------------------------------------------------------
## [요청] data1리스트의 요소에 3을 곱한 값을 새로운 리스트에 저장
## ------------------------------------------------------------------------------
data1 = [1, 2, 3]

## [방법1]------------------------------------------------------------------------
data2 = []              ## 빈 리스트

#   d = 1, 2, 3
for d in data1:
    data2.append(d*3)   ## 데이터 삽입 메서드: append, insert(특정 인덱스 추가)

## [방법2]------------------------------------------------------------------------
data3 = [d*3  for d in data1]
##       -2-  ------1-------

# 확인
print(f'data2 => {data2} / data3 => {data3}')