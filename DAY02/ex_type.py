# ---------------------------------------------
# 내장함수 type() : 데이터의 종류 알려주는 함수
# - 형식 : type(변수명), type(데이터)
# - 반환 : 문자열의 타입명/클래스명
# ---------------------------------------------
## 출력형식 : 12 => 타입정보
print("=== [파이썬 기본 타입] ===")
print(f'12 => {type(12)}')
print(f'12. => {type(12.)}')
print(f'"12" => {type("12")}')
print(f'True => {type(True)}')