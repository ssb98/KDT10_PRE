## -------------------------------------------------------------
## 순서 있는(Sequence) 자료형 - tuple
## - 원소/요소 저장 후 읽기만 가능함!
## - 원소/요소 변경/삭제/추가 안됨!!!
## - 저장 후 변경이 되면 안되는 데이터 타입
## -------------------------------------------------------------
## 튜플 생성 : (데이터1, ..., 데이터n)
##             데이터1, ..., 데이터n
##            (데이터,)
##             데이터,
## -------------------------------------------------------------
data1 = (10, 30, 50)        ## tuple((10, 30, 50)) 인터프리터에서 해줌
data2 = 10, 30
data3 = ('A',)
data4 = 'A',

print(f'data1 : {type(data1)}, {len(data1)}개, {data1}')
print(f'data2 : {type(data2)}, {len(data2)}개, {data2}')
print(f'data3 : {type(data3)}, {len(data3)}개, {data3}')
print(f'data4 : {type(data4)}, {len(data4)}개, {data4}')

## -------------------------------------------------------------
## 튜플 원소/요소 읽기 : 인덱싱, 슬라이싱
## -------------------------------------------------------------
## 원소 1개 읽기 => 변수명[인덱스]
print(f'data1 : {data1}, -2번 원소 : {data1[-2]}')

## 원소 2개 읽기 => 변수명[시작:끝+1:간격]
print(f'data1 : {data1}, 0번, 1번 원소 : {data1[0:2]}')


## -------------------------------------------------------------
## 튜플 원소/요소 변경/삭제 : 불가!
## -------------------------------------------------------------
#data1[0]='A'  ## 미지원 기능
