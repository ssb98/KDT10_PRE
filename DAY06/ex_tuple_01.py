## -------------------------------------------------------------
## 튜플 원소/요소 추가/제거 메서드 : 미지원!
## ==> 지원 메서드 : 원소 갯수 확인 count()
##                  원소 인덱스 찾기 index()
## -------------------------------------------------------------
print(data1.count(11), data1.index(30))