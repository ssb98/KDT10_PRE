## ---------------------------------------------------------------------------
## for ~ in 반복문 : 중첩반복문
## ---------------------------------------------------------------------------
## 9단만 출력 -----------------------------------------------------------------
## 9 * 1 = 9
## 9 * 2 = 18
##   :
## 9 * 9 = 81
for num in range(1, 10):    ## 1<= ~ <10
    print(f'9 * {num} = {9 * num:>2}')    ## :>2 → 2자리로 오른쪽 정렬 포맷

## 2단 ~ 9단 모두 출력 --------------------------------------------------------
for dan in range(2, 10):
    print(f'\n---{dan}---')

    for num in range(1, 10):
        print(f'{dan} * {num} = {dan * num:>2}')

## 홀수단만 출력 3, 5, 7, 9단 -------------------------------------------------
for hol in range(3, 10, 2):
    print(f'\n---{hol}---')

    for num in range(1, 10):
        print(f'{hol} * {num} = {hol * num:>2}')