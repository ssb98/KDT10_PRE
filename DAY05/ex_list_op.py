## -----------------------------------------------------------------------
## 멤버연산자 => 원소/요소가 맞는가 검사 in / not in
## -----------------------------------------------------------------------
dataList=[11, 44, "apple", "summer", False]

##=> 11 데이터가 멤버인지 검사
print(f'11 in dataList => {11 in dataList}')
print(f'apple in dataList => {"apple" in dataList}')
print(f'Apple in dataList => {"Apple" in dataList}')