## ----------------------------------------------------------------------
## if ~ else 조건문과 조건부표현식
## ----------------------------------------------------------------------
## 조건부표현식 : 1줄 조건문
## - 형식 :  True 실행  if 조건식 else  False 실행
## ----------------------------------------------------------------------
## [예시1] 양수, 음수, 0 출력하기
num = 9
if num>0:
    print("양수")
elif num<0:
    print("음수")
else:
    print("영")

## ==> 조건부 표현식
result= "양수" if num>0  else "음수" if num<0 else "영"
